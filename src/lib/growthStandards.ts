// 获取指定月龄的标准值（支持插值）
export function getStandardAtAge(
  gender: 'boy' | 'girl',
  type: 'height' | 'weight',
  ageMonths: number
): GrowthStandardPoint | null {
  const standards = GROWTH_STANDARDS[gender][type];
  
  // 找到最接近的两个点
  const sorted = [...standards].sort((a, b) => a.age - b.age);
  
  // 如果正好匹配
  const exact = sorted.find(s => s.age === ageMonths);
  if (exact) return exact;
  
  // 找到前后两个点进行线性插值
  let lower = sorted[0];
  let upper = sorted[sorted.length - 1];
  
  for (let i = 0; i < sorted.length - 1; i++) {
    if (sorted[i].age <= ageMonths && sorted[i + 1].age >= ageMonths) {
      lower = sorted[i];
      upper = sorted[i + 1];
      break;
    }
  }
  
  if (ageMonths < lower.age || ageMonths > upper.age) return null;
  
  const ratio = (ageMonths - lower.age) / (upper.age - lower.age);
  
  return {
    age: ageMonths,
    p3: lower.p3 + (upper.p3 - lower.p3) * ratio,
    p10: lower.p10 + (upper.p10 - lower.p10) * ratio,
    p25: lower.p25 + (upper.p25 - lower.p25) * ratio,
    p50: lower.p50 + (upper.p50 - lower.p50) * ratio,
    p75: lower.p75 + (upper.p75 - lower.p75) * ratio,
    p90: lower.p90 + (upper.p90 - lower.p90) * ratio,
    p97: lower.p97 + (upper.p97 - lower.p97) * ratio,
  };
}

// 获取百分位标签
export function getPercentileLabel(
  value: number,
  gender: 'boy' | 'girl',
  type: 'height' | 'weight',
  ageMonths: number
): { label: string; color: string } {
  const standard = getStandardAtAge(gender, type, ageMonths);
  if (!standard) return { label: '未知', color: '#999999' };
  
  if (value < standard.p3) return { label: '偏下', color: '#D4836C' };
  if (value < standard.p10) return { label: '中下', color: '#D4836C' };
  if (value < standard.p25) return { label: '偏低', color: '#F7DBA7' };
  if (value < standard.p75) return { label: '正常', color: '#AAB794' };
  if (value < standard.p90) return { label: '偏高', color: '#F7DBA7' };
  if (value < standard.p97) return { label: '中上', color: '#D4836C' };
  return { label: '偏上', color: '#D4836C' };
}

// 计算月龄
export function calculateAgeMonths(birthDate: string, measurementDate: string): number {
  const birth = new Date(birthDate);
  const measure = new Date(measurementDate);
  const months = (measure.getFullYear() - birth.getFullYear()) * 12 + 
                 (measure.getMonth() - birth.getMonth());
  return Math.max(0, months);
}
